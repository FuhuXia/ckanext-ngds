def isNumber(s):
    """
    Checking if a String is a Number
    """
    try:
        float(s)
        return True
    except ValueError:
        pass
    
    return False
# def isNumber(s)

def isInteger(s):
    """
    Checking if a String is an Integer
    """
    try:
        int(s)
        return True
    except ValueError:
        pass
    
    return False
# def isInteger(s)
